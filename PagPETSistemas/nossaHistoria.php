<h2>PET-Sistemas</h2>

<p> O grupo PET-Sistemas teve início no final de 2010 com a seleção de 6 acadêmicos, 
    4 bolsistas e 2 não bolsistas e tendo com tutor o Profº Drº Fábio Henrique Viduani Martinez.</p>

<p> No primeiro ano do grupo, tivemos algumas atividades de destaque como: </p>
<p>
<ul type = "disc">
    <li>Projetos de extensão (CINECLUB e cursos de informática para a população)</li>
    <li>Monitorias nas matérias de Algoritmos e Programação I e II, Introdução aos Sistemas Digitais, Teoria da Computação</li>
    <li>Iniciações cientificas nas áreas de TI Verde e Computação em Nuvem</li>
    <li>Participação em eventos como: II ERI-MS e UFMS de Portas Abertas.</li>
</ul>
</p>
<p>Atualmente, o grupo conta com 7 acadêmicos bolsistas, envolvidos em atividades de extensão, pesquisa, 
    além da criação de uma página Web para o PET e  apoiar o DesTaCom.</p>

